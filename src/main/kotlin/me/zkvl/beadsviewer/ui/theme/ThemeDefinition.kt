package me.zkvl.beadsviewer.ui.theme

/**
 * Defines the available theme modes for the Beads Viewer.
 */
enum class ThemeMode {
    /**
     * Light theme with bright backgrounds and dark text.
     */
    LIGHT,

    /**
     * Dark theme with dark backgrounds and light text.
     */
    DARK,

    /**
     * Automatically follows the IntelliJ IDE theme.
     */
    SYSTEM
}
